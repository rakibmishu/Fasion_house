package bean;

/**
 *
 * @author hp
 */
public class Product {
    private int id;
    private String pname;
    private double price;
    private int qty;

    public Product() {
    }

    public Product(int id, String pname, double price, int qty) {
        this.id = id;
        this.pname = pname;
        this.price = price;
        this.qty = qty;
    }
    
    

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getPname() {
        return pname;
    }

    public void setPname(String pname) {
        this.pname = pname;
    }

    public double getPrice() {
        return price;
    }

    public void setPrice(double price) {
        this.price = price;
    }

    public int getQty() {
        return qty;
    }

    public void setQty(int qty) {
        this.qty = qty;
    }
    
    
    
}
